 <p>
 С++ является расширением языка C, позволяющим реализовать концепцию ООП, а также включающим в себя некоторые дополнительные возможности.
 Часть средств языка C, несмотря на их поддержку C++, признаны устаревшими в контексте новых подходов к программированию и потому употребляются 
 ред- ко, как правило, заменяясь более новыми средствами C++. В результате разли- чия между C и C++ оказываются более значительными, чем кажется
 на первый взгляд. 
На практике существует гораздо больше различий между C и C++, чем мо- жет показаться вначале. Несмотря на то что можно писать программы на C++,
 внешне напоминающие программы на C, вряд ли кому-то придет в голову так поступать. Программисты, использующие C++, не только пользуются преимуществами
 этого языка перед C, но и по-новому используют возможности, уна- следованные от C. 
 </p>