<h1>С и С++</h1>
<h2>Чем отличаются C и C++?</h2>
<p>
С++ унаследовал возможности языка C. Строго говоря, C++ является расшире- нием языка С: любая конструкция на языке C является корректной в C++;
 в то же время обратное неверно. Наиболее значительные нововведения, присутст- вующие в C++, касаются классов, объектов и ООП (первоначальное
 название языка C++ — «С с классами»). Тем не менее имеются и другие усовершенствова- ния, связанные со способами организации ввода/вывода
 и написанием коммен- тариев. Иллюстрация отношения между языками C и C++ приведена на рисунке из книги Лафоре ООП
 
 
 </p>
 
